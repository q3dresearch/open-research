PIPELINE_HEADER = '''"""Cumulative transform pipeline — re-runnable from raw data.

Auto-generated by agents/deep_analyst.py. Each step_NN function was proposed
by an LLM and reviewed/editable by a human.
"""

import pandas as pd
import numpy as np
import re
import math

'''

PIPELINE_FOOTER_TEMPLATE = """
STEPS = [{step_list}]


def run_pipeline(df: pd.DataFrame) -> pd.DataFrame:
    for step in STEPS:
        df = step(df)
    return df
"""


def get_phase_dir(dataset_id: str) -> Path:
    d = ARTIFACTS_DIR / dataset_id / PHASE_DIR
    d.mkdir(parents=True, exist_ok=True)
    return d


def load_pipeline(dataset_id: str) -> tuple[str, list[str]]:
    """Load existing pipeline.py. Returns (full_code, list_of_step_names)."""
    path = get_phase_dir(dataset_id) / "pipeline.py"
    if not path.exists():
        return "", []
    code = path.read_text()
    names = _re.findall(r"^def (step_\w+)\(", code, _re.MULTILINE)
    return code, names


def _extract_func_pairs(pipeline_code: str, step_names: list[str]) -> list[tuple[str, str]]:
    """Extract (name, code) pairs from pipeline source, stripping header/footer."""
    if not pipeline_code:
        return []
    # Strip footer (STEPS = [...] onward)
    body = _re.split(r"\nSTEPS\s*=\s*\[", pipeline_code)[0]
    # Strip header (everything before first def)
    first_def = body.find("\ndef ")
    if first_def >= 0:
        body = body[first_def + 1:]
    # Split into function blocks
    blocks = _re.split(r"(?=^def step_)", body, flags=_re.MULTILINE)
    pairs = []
    for block in blocks:
        block = block.strip()
        if not block:
            continue
        m = _re.match(r"def (step_\w+)\(", block)
        if m:
            pairs.append((m.group(1), block))
    return pairs


def save_pipeline(dataset_id: str, step_funcs: list[tuple[str, str]]):
    """Write pipeline.py from a list of (func_name, func_code) tuples."""
    path = get_phase_dir(dataset_id) / "pipeline.py"
    body = PIPELINE_HEADER
    for _, code in step_funcs:
        body += code.rstrip() + "\n\n\n"
    step_list = ", ".join(name for name, _ in step_funcs)
    body += PIPELINE_FOOTER_TEMPLATE.format(step_list=step_list)
    path.write_text(body)
    return path


# ---------------------------------------------------------------------------
# State tracking (lightweight JSON, not full CSV)
# ---------------------------------------------------------------------------

def load_state(dataset_id: str) -> dict | None:
    """Load state.json if it exists."""
    path = get_phase_dir(dataset_id) / "state.json"
    if path.exists():
        return json.loads(path.read_text())
    return None


def save_state(dataset_id: str, df: pd.DataFrame, raw_df: pd.DataFrame,
               step_log: list[dict]) -> Path:
    """Save state.json: schema tracking + sample records."""
    original_cols = list(raw_df.columns)
    current_cols = list(df.columns)
    added = [c for c in current_cols if c not in original_cols]

    # Sample records (first 5 rows, JSON-safe)
    sample = json.loads(df.head(5).to_json(orient="records", date_format="iso"))

    state = {
        "row_count": len(df),
        "original_columns": original_cols,
        "current_columns": current_cols,
        "added_columns": [
            {"name": c, "dtype": str(df[c].dtype)}
            for c in added
        ],
        "steps_applied": len(step_log),
        "step_log": step_log,
        "sample_records": sample,
    }

    path = get_phase_dir(dataset_id) / "state.json"
    path.write_text(json.dumps(state, indent=2))
    return path


def get_chain_summary(conn, dataset_id: str) -> str:
    """Build a summary of prior engineer runs."""
    rows = conn.execute(
        """SELECT id, verdict, verdict_reason, llm_response
           FROM runs WHERE dataset_id = ? AND action = ?
           ORDER BY finished_at""",
        (dataset_id, ACTION),
    ).fetchall()
    if not rows:
        return "(first engineer run — no prior runs)"
    lines = []
    for r in rows:
        try:
            resp = json.loads(r["llm_response"])
            hypothesis = resp.get("hypothesis", "?")
            findings = resp.get("findings", [])
        except (json.JSONDecodeError, TypeError):
            hypothesis, findings = "?", []
        lines.append(f"- run {r['id']}: {r['verdict']} — hypothesis: {hypothesis}")
        lines.append(f"  reason: {r['verdict_reason'][:120]}")
        for f in findings[:2]:
            lines.append(f"    {f[:100]}")
    return "\n".join(lines)


# ---------------------------------------------------------------------------
# Step execution
# ---------------------------------------------------------------------------

def execute_step(df: pd.DataFrame, step_code: str, step_num: int) -> tuple[pd.DataFrame, str, str | None]:
    """Execute a single step function. Returns (df_after, result_msg, func_name)."""
    namespace = {"pd": pd, "np": __import__("numpy"), "re": __import__("re"), "math": __import__("math")}
    try:
        exec(step_code, namespace)
        step_fn = None
        for k, v in namespace.items():
            if callable(v) and k.startswith("step_"):
                step_fn = v
                break
        if step_fn is None:
            raise RuntimeError("No step_XX function found in generated code")
        df_after = step_fn(df.copy())
        new_cols = [c for c in df_after.columns if c not in df.columns]
        result = f"OK: +{len(new_cols)} cols ({', '.join(new_cols)})" if new_cols else "OK: in-place transform"
        return df_after, result, step_fn.__name__
    except Exception as e:
        return df, f"FAILED: {type(e).__name__}: {e}", None


