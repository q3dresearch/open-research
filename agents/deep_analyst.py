#!/usr/bin/env python3
"""
Level 2 Deep Analyst — run-based investigation architecture.

Each invocation is a RUN: a multi-step investigation testing one hypothesis.
  1. Reads current state (pipeline.py + state.json)
  2. LLM plans the run: hypothesis + ordered steps
  3. Executes each step, updating pipeline.py + state.json
  4. Generates charts after all steps
  5. LLM evaluates the run holistically
  6. Writes run artifact

Usage:
    python -m agents.deep_analyst <dataset_id> [--max-steps N] [--max-runs N]
"""

